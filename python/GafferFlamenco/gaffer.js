// This file must be installed as `{$FLAMENCO_MANAGER_ROOT}/scripts/gaffer.js`,
// where `$FLAMENCO_MANAGER_ROOT` is the directory which the manager is run
// from.

const JOB_TYPE = {

	label : "Gaffer",
	description : "Runs tasks for Gaffer's FlamencoDispatcher node.",
	settings : [
		{
			key: "tasks",
			type: "list",
			default : [],
			description : "The task definitions generated by the FlamencoDispatcher node.",
		},
	],
};

function compileJob( job ) {

	let tasks = {};

	for( const taskDefinition of job.settings.tasks ) {

		const task = author.Task( taskDefinition["name"], "misc" );
		tasks[taskDefinition["name"]] = task;

		if( "commandArgs" in taskDefinition )
		{
			const command = author.Command(
				"exec",
				{
					exe : "gaffer",
					args : taskDefinition["commandArgs"],
				}
			);
			task.addCommand( command );
		}

		if( "dependencies" in taskDefinition )
		{
			for( const dependencyName of taskDefinition["dependencies"] )
			{
				task.addDependency( tasks[dependencyName] );
			}
		}

		job.addTask( task );

		// The `tasks` field isn't really human readable - remove it
		// so it doesn't clutter the web UI.

		delete job.settings["tasks"];

	}
}

